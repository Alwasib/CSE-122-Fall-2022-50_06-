#include<iostream>
#include<string>
using namespace std;
int main()
{
    int x=0,n,i;
    string s;
    cin>>n;
    for(i=0;i<n;i++)
    {
        cin>>s;
        if(s[1]=='+')
        {
            x=x+1;
        }
        else if(s[1]=='-')
        {
            x=x-1;
        }

    }

    cout<<x;

}
